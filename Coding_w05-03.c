#include <stdio.h> // เรียกใช้ไลบรารี standard input/output (stdio.h) ซึ่งเป็นส่วนสำคัญสำหรับฟังก์ชันการรับและแสดงผลข้อมูล เช่น printf()

int main() { // ฟังก์ชัน main() เป็นจุดเริ่มต้นการทำงานของโปรแกรมทุกโปรแกรมที่เขียนด้วยภาษา C
    // บรรทัดที่ 1: a = 0.617%
    // Output ที่ต้องการ: "a= 0.617%" (มีเว้นวรรค 1 ช่องหลัง '=')
    printf("a=%6.3f%%\n", 0.617); // ใช้ printf เพื่อแสดงผลตัวอักษร 'a=' ตามด้วยค่าทศนิยม 0.617 จัดรูปแบบด้วย %6.3f ซึ่งหมายถึงมีความกว้างรวม 6 ช่อง (รวมจุดทศนิยมและตัวเลข) แสดง 3 ตำแหน่งทศนิยม และจะชิดขวาภายในช่องนั้น เมื่อค่า 0.617 มี 5 ตัวอักษรพอดี จะมีเว้นวรรค 1 ช่องนำหน้า ทำให้ได้รูปแบบที่ต้องการ ตามด้วยสัญลักษณ์ '%%' เพื่อแสดงเครื่องหมาย '%'
                                  // ผลลัพธ์: "a= 0.617%"

    // บรรทัดที่ 2: b = 25%
    // Output ที่ต้องการ: "b=  25%" (มีเว้นวรรค 2 ช่องหลัง '=')
    printf("b=%4d%%\n", 25);     // ใช้ printf เพื่อแสดงผลตัวอักษร 'b=' ตามด้วยค่าจำนวนเต็ม 25 จัดรูปแบบด้วย %4d ซึ่งหมายถึงมีความกว้างรวม 4 ช่อง และจะชิดขวาภายในช่องนั้น เมื่อค่า 25 มี 2 ตัวอักษร จะมีเว้นวรรค 2 ช่องนำหน้า ทำให้ได้รูปแบบที่ต้องการ ตามด้วยสัญลักษณ์ '%%'
                                  // ผลลัพธ์: "b=  25%"

    // บรรทัดที่ 3: c = 19.000%
    // Output ที่ต้องการ: "c = 19.000%" (มีเว้นวรรค 1 ช่องหลัง 'c' และ 1 ช่องหลัง '=')
    printf("c = %6.3f%%\n", 19.000); // ใช้ printf เพื่อแสดงผลตัวอักษร 'c = ' (สังเกตว่ามีการเว้นวรรค 1 ช่องหลัง 'c' และ 1 ช่องหลัง '=') ตามด้วยค่าทศนิยม 19.000 จัดรูปแบบด้วย %6.3f ซึ่งหมายถึงมีความกว้างรวม 6 ช่อง แสดง 3 ตำแหน่งทศนิยม และจะชิดขวาภายในช่องนั้น เมื่อค่า 19.000 มี 6 ตัวอักษรพอดี จะไม่มีเว้นวรรคนำหน้า ตามด้วยสัญลักษณ์ '%%'
                                  // ผลลัพธ์: "c = 19.000%"

    // บรรทัดที่ 4: d = 9%
    // Output ที่ต้องการ: "d   =  9%" (มีเว้นวรรค 2 ช่องหลัง 'd' และ 2 ช่องหลัง '=')
    printf("d   =  %d%%\n", 9); // ใช้ printf เพื่อแสดงผลตัวอักษร 'd   =  ' (ซึ่งมีเว้นวรรค 2 ช่องหลัง 'd' และ 2 ช่องหลัง '=') ตามด้วยค่าจำนวนเต็ม 9 จัดรูปแบบด้วย %d (ซึ่งจะใช้ความกว้างเท่ากับตัวเลขพอดี) แล้วตามด้วยสัญลักษณ์ '%%' เพื่อให้ได้รูปแบบตรงตามตำแหน่งที่กำหนด
                                  // ผลลัพธ์: "d   =  9%"

    // บรรทัดที่ 5: e = 124.555%
    // Output ที่ต้องการ: "e= 124.555%" (มีเว้นวรรค 1 ช่องหลัง '=')
    printf("e= %7.3f%%\n", 124.555); // ใช้ printf เพื่อแสดงผลตัวอักษร 'e= ' (สังเกตว่ามีการเว้นวรรค 1 ช่องหลัง '=') ตามด้วยค่าทศนิยม 124.555 จัดรูปแบบด้วย %7.3f ซึ่งหมายถึงมีความกว้างรวม 7 ช่อง แสดง 3 ตำแหน่งทศนิยม และจะชิดขวาภายในช่องนั้น เมื่อค่า 124.555 มี 7 ตัวอักษรพอดี จะไม่มีเว้นวรรคนำหน้า ตามด้วยสัญลักษณ์ '%%'
                                  // ผลลัพธ์: "e= 124.555%"

    // บรรทัดที่ 6: f = 0.001%
    // Output ที่ต้องการ: "f= 0.001%" (มีเว้นวรรค 1 ช่องหลัง '=')
    printf("f= %5.3f%%\n", 0.001); // ใช้ printf เพื่อแสดงผลตัวอักษร 'f= ' (สังเกตว่ามีการเว้นวรรค 1 ช่องหลัง '=') ตามด้วยค่าทศนิยม 0.001 จัดรูปแบบด้วย %5.3f ซึ่งหมายถึงมีความกว้างรวม 5 ช่อง แสดง 3 ตำแหน่งทศนิยม และจะชิดขวาภายในช่องนั้น เมื่อค่า 0.001 มี 5 ตัวอักษรพอดี จะไม่มีเว้นวรรคนำหน้า ตามด้วยสัญลักษณ์ '%%'
                                  // ผลลัพธ์: "f= 0.001%"

    return 0; // คืนค่า 0 เพื่อบอกระบบปฏิบัติการว่าโปรแกรมทำงานเสร็จสมบูรณ์โดยไม่มีข้อผิดพลาดเกิดขึ้น
}