#include <stdio.h> // เรียกใช้ไลบรารี standard input/output (stdio.h) ซึ่งเป็นส่วนสำคัญที่รวบรวมฟังก์ชันสำหรับการรับและแสดงผลข้อมูล

int main() { // ฟังก์ชัน main() เป็นจุดเริ่มต้นการทำงานของโปรแกรมทุกโปรแกรมที่เขียนด้วยภาษา C
    // --- ส่วนประกาศตัวแปร ---
    char name[30];      // ประกาศตัวแปรอาร์เรย์ 'name' ชนิด char เพื่อเก็บชื่อ (เป็นข้อความหลายตัวอักษร)
    int age;            // ประกาศตัวแปร 'age' ชนิด int (จำนวนเต็ม) เพื่อเก็บค่าอายุ
    float height;       // ประกาศตัวแปร 'height' ชนิด float (เลขทศนิยม) เพื่อเก็บค่าส่วนสูง (หน่วยเป็นเซนติเมตร)
    char subject[30];   // ประกาศตัวแปรอาร์เรย์ 'subject' ชนิด char เพื่อเก็บชื่อวิชา (เป็นข้อความหลายตัวอักษร)
    float grade;        // ประกาศตัวแปร 'grade' ชนิด float เพื่อเก็บค่าเกรด (เป็นเลขทศนิยม)
    char gradeChar;     // ประกาศตัวแปร 'gradeChar' ชนิด char เพื่อเก็บสัญลักษณ์เกรดเป็นตัวอักษร (เช่น 'A', 'B') ซึ่งจะคำนวณจากค่า grade

    // --- ส่วนรับข้อมูลจากผู้ใช้ (2 บรรทัด) ---
    // บรรทัดที่ 1: รับข้อมูลส่วนบุคคล (ชื่อ, อายุ, ส่วนสูง)
    printf("กรุณาป้อน ชื่อ อายุ และส่วนสูง (คั่นด้วยช่องว่าง): "); // แสดงข้อความแจ้งให้ผู้ใช้ป้อนข้อมูลส่วนบุคคล
    scanf("%s %d %f", name, &age, &height); // อ่านค่าชื่อ (สตริง), อายุ (จำนวนเต็ม), ส่วนสูง (ทศนิยม) ที่ผู้ใช้ป้อนเข้ามา

    // บรรทัดที่ 2: รับข้อมูลผลการเรียน (ชื่อวิชา, เกรด)
    printf("กรุณาป้อน ชื่อวิชา และเกรด (คั่นด้วยช่องว่าง): "); // แสดงข้อความแจ้งให้ผู้ใช้ป้อนข้อมูลผลการเรียน
    scanf("%s %f", subject, &grade); // อ่านค่าชื่อวิชา (สตริง) และเกรด (ทศนิยม) ที่ผู้ใช้ป้อนเข้ามา

    // --- ส่วนประมวลผล (คำนวณสัญลักษณ์เกรด) ---
    // ใช้ Conditional (Ternary) Operator เพื่อกำหนดค่า 'gradeChar' ตามช่วงคะแนนของ 'grade'
    // หาก grade >= 90 จะได้ 'A', หาก >= 80 ได้ 'B', หาก >= 70 ได้ 'C', หาก >= 60 ได้ 'D', นอกนั้นได้ 'F'
    gradeChar = (grade >= 90) ? 'A' : 
                (grade >= 80) ? 'B' : 
                (grade >= 70) ? 'C' : 
                (grade >= 60) ? 'D' : 'F';

    // --- ส่วนแสดงผลลัพธ์ (2 บรรทัด) ---
    // บรรทัดที่ 1: แสดงข้อมูลส่วนบุคคล (ตัวอักษรตัวแรกของชื่อ, อายุ, ส่วนสูงทศนิยม 1 ตำแหน่ง)
    printf(">> %c is %d years old and %.1f centimeters tall.\n", name[0], age, height); 
    // %c จะแสดงผลตัวอักษรตัวแรกของชื่อ (name[0])
    // %d จะแสดงผลอายุ
    // %.1f จะแสดงผลส่วนสูงในรูปแบบทศนิยม 1 ตำแหน่ง

    // บรรทัดที่ 2: แสดงข้อมูลผลการเรียน (ชื่อวิชา, เกรดทศนิยม 2 ตำแหน่ง, สัญลักษณ์เกรด)
    printf(">> In subject %c, %c got %.2f which is symbolized as '%c'.\n", subject[0], name[0], grade, gradeChar);
    // %c ตัวแรก แสดงผลตัวอักษรตัวแรกของชื่อวิชา (subject[0])
    // %c ตัวที่สอง แสดงผลตัวอักษรตัวแรกของชื่อนักเรียน (name[0])
    // %.2f แสดงผลเกรดในรูปแบบทศนิยม 2 ตำแหน่ง
    // %c ตัวสุดท้าย แสดงผลสัญลักษณ์เกรดที่คำนวณไว้ (gradeChar)

    return 0; // คืนค่า 0 เพื่อบอกระบบปฏิบัติการว่าโปรแกรมทำงานเสร็จสมบูรณ์และไม่มีข้อผิดพลาดเกิดขึ้น
}