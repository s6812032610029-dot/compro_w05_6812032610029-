#include <stdio.h> // เรียกใช้ไลบรารี standard input/output (stdio.h) ซึ่งเป็นส่วนสำคัญที่รวบรวมฟังก์ชันสำหรับการรับและแสดงผลข้อมูล เช่น ฟังก์ชัน printf()

int main() { // ฟังก์ชัน main() เป็นจุดเริ่มต้นการทำงานของโปรแกรมทุกโปรแกรมที่เขียนด้วยภาษา C
    char name = 'J';    // ประกาศตัวแปร 'name' ชนิด char (ตัวอักษรเดี่ยว) เพื่อเก็บตัวอักษรตัวแรกของชื่อเล่นตามที่โจทย์กำหนด (ในที่นี้คือ 'J' ซึ่งเป็นตัวพิมพ์ใหญ่)
    int age = 20;       // ประกาศตัวแปร 'age' ชนิด int (จำนวนเต็ม) เพื่อเก็บค่าอายุ (ในที่นี้คือ 20 ปี) ตามที่โจทย์กำหนด
    double weight = 55.5; // ประกาศตัวแปร 'weight' ชนิด double (เลขทศนิยมที่มีความแม่นยำสูง) เพื่อเก็บค่าน้ำหนัก (ในที่นี้คือ 55.5 กิโลกรัม) ตามที่โจทย์กำหนด

    // ส่วนนี้ใช้แสดงผลบรรทัดแรกตามรูปแบบที่กำหนด: "Student J is 20 years old."
    printf("Student %c is %d years old.\n", name, age); 
    
    // ส่วนนี้ใช้แสดงผลบรรทัดที่สองตามรูปแบบที่กำหนด: "His weighs is 55.5 kg."
    printf("His weighs is %.1f kg.\n", weight); 

    return 0; // คืนค่า 0 เพื่อบอกระบบปฏิบัติการว่าโปรแกรมทำงานเสร็จสมบูรณ์และไม่มีข้อผิดพลาดเกิดขึ้น
}